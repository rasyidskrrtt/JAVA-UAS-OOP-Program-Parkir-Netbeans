        private void tampil(){
        String url = "jdbc:mysql://localhost:3306/adminparkir";
        Object[] header = {"Kode Parkir", "Nomor Plat", "Jenis", "Biaya Parkir", "Total Jam Parkir"};
        model = new DefaultTableModel(null, header);
        table.setModel(model);
        try {
            konek = DriverManager.getConnection(url, "root", "");
            stat = konek.createStatement();
            String sql1 = "SELECT kodeParkir, nomorPlat, jenis, biayaParkir, totalJamParkir FROM parkirkeluar";
            hasil = stat.executeQuery(sql1);
            while (hasil.next()){
                Object[] o = new Object[9];
                o[0] = hasil.getString("kodeParkir");
                o[1] = hasil.getString("nomorPlat");
                o[2] = hasil.getString("jenis");
                o[3] = hasil.getString("biayaParkir");
                o[4] = hasil.getString("totalJamParkir");
                model.addRow(o);
        }
            hasil.close();
            stat.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Menampilkan data gagal", "Informasi", JOptionPane.INFORMATION_MESSAGE);
        }
    }
        
